HORMV	=	2
SPEED	=	3
MAX1	=	20
MAX2	=	30
DATA	=	59000
DATANUM	=	4

	ORG	58000

	CALL	INIT

MAIN	XOR	A
	LD	(65450),A
	CALL	KEYIN
	CALL	YOU
	CALL	FIRST
	CALL	62750
	JP	MAIN

BOMBS	LD	A,(MAX1)
	LD	(PTR),A
BOMBL	LD	A,(PTR)
	CALL	LOAD
	CALL	BOMB1
	LD	A,(PTR)
	CALL	SAVE
	LD	A,(PTR)
	INC	A
	LD	(PTR),A
	JP	NZ,BOMBL
	RET

BOMB1	LD	A,(FLAG)

KEYIN	DI
	IN	178
	OR	1
	OUT	178
	LD	A,191
	OUT	177
	IN	225
	CPL
	LD	(KEYSP),A
	LD	A,254
	OUT	177
	IN	225
	CPL
	LD	(KEY2),A
	LD	A,253
	OUT	177
	IN	225
	CPL
	LD	(KEY1),A
	EI
	RET

YOU	LD	HL,(YOUX)
	LD	(XP),HL
	XOR	A
	LD	(ONOFF),A
	LD	A,4
	LD	(SHNUM),A
	CALL	PLOT
	XOR	A
	LD	(XM),A
	LD	(YM),A
	LD	A,(KEY1)
	AND	1
	JP	Z,NOTUP
	LD	A,(YOUY)
	OR	A
	JP	Z,NOTUP
	DEC	A
	LD	(YOUY),A
NOTUP	LD	A,(KEY2)
	AND	1
	JP	Z,NOTDN
	LD	A,(YOUY)
	CP	45
	JP	Z,NOTDN
	INC	A
	LD	(YOUY),A
NOTDN	LD	A,(KEY1)
	AND	128
	JP	Z,NOTL
	LD	A,(YOUX)
	OR	A
	JP	Z,NOTL
	SUB	HORMV
	LD	(YOUX),A
	XOR	A
	SUB	HORMV
	LD	(XM),A
NOTL	LD	A,(KEY2)
	AND	128
	JP	Z,NOTR
	LD	A,(YOUX)
	CP	128
	JP	Z,NOTR
	ADD	HORMV
	LD	(YOUX),A
	LD	A,HORMV
	LD	(XM),A
NOTR	LD	HL,(YOUX)
	LD	(XP),HL
	LD	A,4
	LD	(SHNUM),A
	LD	A,1
	LD	(ONOFF),A
	CALL	PLOT
	RET

LOAD	LD	L,A
	LD	H,0
	ADD	HL,HL
	ADD	HL,HL
	LD	DE,DATA
	ADD	HL,DE
	LD	DE,X1
	LD	B,DATANUM
LDIR	LD	A,(HL)
	LD	(DE),A
	INC	HL
	INC	DE
	DEC	B
	JP	NZ,LDIR
	RET

SAVE	LD	L,A
	LD	H,0
	ADD	HL,HL
	ADD	HL,HL
	LD	DE,DATA
	ADD	HL,DE
	LD	DE,X1
	LD	B,DATANUM
	EX	DE,HL
	JP	LDIR

FIRST	XOR	A
	LD	(PTR),A
FIRSTL	LD	A,(PTR)
	CALL	LOAD
	CALL	FIRST1
	LD	A,(PTR)
	CALL	SAVE
	LD	HL,PTR
	INC	(HL)
	LD	A,(HL)
	CP	MAX1
	JP	NZ,FIRSTL
	RET

FIRST1	LD	A,(TYPE)
	OR	A
	JP	NZ,NOTFIRB
	LD	A,(FLAG)
	OR	A
	RET	Z
	LD	HL,(X1)
	LD	(XP),HL
	CALL	RND
	AND	1
	SUB	2
	ADD	L
	SUB	SPEED
	LD	(X1),A
	CALL	RND
	AND	32
	JP	Z,NOYMV
	CALL	RND
	AND	64
	JP	Z,MVYUP
	LD	A,(Y1)
	INC	A
	LD	(Y1),A
	CP	50
	JP	NZ,NOYMV
	LD	A,250
	LD	(X1),A
	CALL	RND
	AND	32
	ADD	15
	LD	(Y1),A
	JP	NOYMV
MVYUP	LD	A,(Y1)
	DEC	A
	LD	(Y1),A
	OR	A
	JP	NZ,NOYMV
	LD	A,250
	LD	(X1),A
	CALL	RND
	AND	32
	ADD	15
	LD	(Y1),A
NOYMV	XOR	A
	LD	(ONOFF),A
	XOR	A
	LD	(SHNUM),A
	CALL	PLOT
	LD	HL,(X1)
	LD	(XP),HL
	LD	A,1
	LD	(ONOFF),A
	CALL	PLOT
	RET
NOTFIRB	CP	4
	JP	NC,NOTBUI
	LD	A,(FLAG)
	OR	A
	JP	NZ,DOFIR
	XOR	A
SRCH1	PUSH	AF
	CALL	LOAD
	LD	A,(FLAG)
	OR	A
	JP	Z,VALID
	LD	A,(X1)
	CP	220
	JP	C,VALID
	POP	AF
	LD	A,(PTR)
	CALL	LOAD
	RET
VALID	POP	AF
	INC	A
	CP	MAX1
	JP	NZ,SRCH1
	CALL	RND
	AND	7
	RET	NZ
INGND	CALL	RND
	AND	3
	JP	Z,INGND
	LD	(TYPE),A
	LD	A,234
	LD	(X1),A
	LD	A,1
	LD	(FLAG),A
	LD	A,(PTR)
	CALL	SAVE
DOFIR	LD	A,(X1)
	LD	(XP),A
	SUB	SPEED
	LD	(X1),A
	LD	A,52
	LD	(YP),A
	XOR	A
	LD	(ONOFF),A
	LD	A,(TYPE)
	LD	(SHNUM),A
	CALL	PLOT
	LD	A,(X1)
	CP	240
	JP	C,NONUBU
	XOR	A
	LD	(FLAG),A
	RET
NONUBU	LD	(XP),A
	LD	A,1
	LD	(ONOFF),A
	CALL	PLOT
	RET
NOTBUI	RET

RND	PUSH	HL
	ADC	(HL)
	LD	HL,RR
	ADC	(HL)
	LD	(HL),A
	INC	HL
	ADD	(HL)
	LD	(HL),A
	INC	HL
	ADC	(HL)
	LD	(HL),A
	POP	HL
	RET

; XP,YP,SHNUM,ONOFF-ARGUMENTS
; COLLIS-COLLISION REGISTER

PLOT	LD	C,0
	LD	B,8
	LD	A,(SHNUM)
	LD	L,A
	LD	H,0
	ADD	HL,HL
	ADD	HL,HL
	ADD	HL,HL
	LD	DE,SHTABS
	ADD	HL,DE
	LD	(SHPTR),HL
	LD	A,(YP)
	RRC
	RRC
	RRC
	AND	7
	ADD	237
	LD	H,A
	LD	A,(XP)
	LD	L,A
	LD	(SCPTR),HL
PLOTLP	LD	HL,(SHPTR)
	LD	A,(HL)
	INC	HL
	LD	(SHPTR),HL
	LD	L,A
	LD	H,0
	LD	A,(YP)
	AND	7
	JP	Z,NOROT
ROTLP	ADD	HL,HL
	DEC	A
	JP	NZ,ROTLP
NOROT	EX	DE,HL
	LD	HL,(SCPTR)
	LD	A,(ONOFF)
	OR	A
	JP	Z,ERASE
	LD	A,(HL)
	AND	E
	OR	C
	LD	C,A
	LD	A,(HL)
	OR	E
	LD	(HL),A
	INC	H
	LD	A,H
	CP	245
	JP	Z,END1
	LD	A,(HL)
	AND	D
	OR	C
	LD	C,A
	LD	A,(HL)
	OR	D
	LD	(HL),A
END1	LD	HL,(SCPTR)
	INC	L
	LD	(SCPTR),HL
	DEC	B
	JP	NZ,PLOTLP
	LD	A,C
	LD	(COLLIS),A
	RET
ERASE	LD	A,E
	CPL
	AND	(HL)
	LD	(HL),A
	INC	H
	LD	A,H
	CP	245
	JP	Z,END1
	LD	A,D
	CPL
	AND	(HL)
	LD	(HL),A
	JP	END1

INIT	XOR	A
	LD	(TYTY),A
	LD	HL,5200
	LD	(YOUX),HL
	CALL	CLS
	CALL	GROUND
	LD	A,1
	LD	(FLAG),A
INL1	PUSH	AF
AGEN	CALL	RND
	AND	63
	CP	52
	JP	NC,AGEN
	LD	(Y1),A
	LD	HL,TYTY
	INC	(HL)
	LD	A,(HL)
	CP	3
	JP	NZ,OKTY
	XOR	A
	LD	(HL),A
OKTY	LD	(TYPE),A
	LD	A,1
	LD	(FLAG),A
	JP	Z,LEAVE
	XOR	A
	LD	(FLAG),A
LEAVE	CALL	RND
	AND	127
	ADD	128
	LD	(X1),A
	POP	AF
	PUSH	AF
	CALL	SAVE
	POP	AF
	INC	A
	JP	NZ,INL1
	RET

GROUND	LD	HL,62464
	LD	A,(OFFS)
	INC	A
	LD	(OFFS),A
	AND	1
	JP	Z,ZERO
	LD	A,248
ZERO	LD	B,240
GNDL	LD	(HL),A
	XOR	248
	INC	L
	DEC	B
	JP	NZ,GNDL
	RET

CLS	LD	HL,60672
	LD	DE,60673
	LD	BC,2047
	LD	(HL),0
	JP	27611

; 0     - LITTLE SHIPS
; 1,2,3 - BUILDING SHAPES
; 4 -------

SHTABS	DFB	6,15,9,9,6,2,4,2

	DFB	252,132,132,132
	DFB	132,132,132,252

	DFB	32,240,136,132
	DFB	132,136,240,32

	DFB	252,248,240,224
	DFB	224,240,248,252

	DFB	9,18,30,28,28,24,8,8

	DFB	24,60,126,255
	DFB	255,126,60,24

; END OF SHAPE DEFINITIONS

XM	DS	1
YM	DS	1
YOUX	DS	1
YOUY	DS	1

RR	DS	3
OFFS	DS	1
PTR	DS	1
TYTY	DS	1

COLLIS	DS	1
SHNUM	DS	1
XP	DS	1
YP	DS	1
ONOFF	DS	1
SHPTR	DS	2
SCPTR	DS	2

